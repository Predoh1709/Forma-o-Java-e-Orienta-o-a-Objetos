
public class TestarLacos {
	public static void main(String[] args) {
		for(int multiplocador = 1; multiplocador <= 10; multiplocador++) {
			for (int contador = 0; contador <= 10; contador++) {
				System.out.print(multiplocador * contador);
				System.out.println(" ");
			}
			System.out.println();
		}
	}
}
